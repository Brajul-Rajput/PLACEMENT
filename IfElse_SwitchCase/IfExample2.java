package IfElse_SwitchCase;

public class IfExample2 {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        if(a>b){
            System.out.println("a greater than b");
        }
        if(a<b){
            System.out.println("a less than b");
        }
        if(a==b){
            System.out.println("a is equal to b");
        }
    }
}
