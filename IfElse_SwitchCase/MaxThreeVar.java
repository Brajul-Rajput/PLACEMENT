package IfElse_SwitchCase;

public class MaxThreeVar {
    public static void main(String[] args) {
        int a=20;
        int b=45;
        int max=0;
        if(a>b){
            max=a;
            System.out.println(max);
        }
        else{
            max=b;
            System.out.println(max);
        }
    }
}
